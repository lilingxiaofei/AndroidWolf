package com.chunlangjiu.app.order;

public class RefundsBean {
    private String hongbao_fee;
    private String refund_fee;
    private String refund_money;
    private String status;
    private String total_price;

    public String getHongbao_fee() {
        return hongbao_fee;
    }

    public void setHongbao_fee(String hongbao_fee) {
        this.hongbao_fee = hongbao_fee;
    }

    public String getRefund_fee() {
        return refund_fee;
    }

    public void setRefund_fee(String refund_fee) {
        this.refund_fee = refund_fee;
    }

    public String getRefund_money() {
        return refund_money;
    }

    public void setRefund_money(String refund_money) {
        this.refund_money = refund_money;
    }

    public String getStatus() {
        return status;
    }

    public void setStatus(String status) {
        this.status = status;
    }

    public String getTotal_price() {
        return total_price;
    }

    public void setTotal_price(String total_price) {
        this.total_price = total_price;
    }
}
